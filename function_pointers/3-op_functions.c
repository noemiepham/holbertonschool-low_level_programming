#include <stdio.h>
#include "3-calc.h"

/**
*op_add - addition
* @a : Number
* @b : Number
* Return: a + b
*/
int op_add(int a, int b)
	{
		return (a + b);
	}
/**
* op_sub - subtraction
* @a : Number
* @b : Number
* Return: a - b
*/
int op_sub(int a, int b)
	{
		return (a - b);
	}
/**
* op_mul - multiplication
* @a : Number
* @b : Number
* Return: a * b
*/
int op_mul(int a, int b)
	{
		return (a * b);
	}
/**
* op_div - divisions
* @a : Number
* @b : Number
* Return: a / b
*/
int op_div(int a, int b)
	{
		return (a / b);
	}
/**
* op_mod - modolo
* @a : Number
* @b : Number
* Return: a % b
*/
int op_mod(int a, int b)
	{
		return (a % b);
	}
